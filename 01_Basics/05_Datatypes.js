// All datatypes in javascript can be categorized into two main types:
// 1. Primitive datatypes
// 2. Reference datatypes


/* 
    Primitive datatypes include: call by value 
    Number, String, Boolean, Undefined, Null, Symbol, BigInt
    Reference datatypes include: call by reference
    Object, Array, Function
*/

const myFunc = function() {
    console.log("Hello, World!");
}
myFunc(); // This will call the function and print "Hello, World!"

